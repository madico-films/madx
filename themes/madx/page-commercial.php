<script>
var zip = document.getElementById("zip");
var api_key = '<?php echo $GOOGLE_API_KEY; ?>'

function getLocation() {
    if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition(getPosition);
    } else {
        alert("Geolocation is not supported by this browser. Please manually type in your zip code and press Enter");
    }
}
function getPosition(position) {
    var apiURL = 'https://maps.googleapis.com/maps/api/geocode/json?latlng='+ position.coords.latitude +','+ position.coords.longitude +'&key=' + api_key;

    $.ajax({
    	url: apiURL,
    	success: function(data){
    		var currentZip = data.results[0].address_components[7].long_name;
    		// After turn lat/long into a zip code, call sendZip
    		// to get all zip codes in a set radius
    		sendZip(currentZip);
    	},
    	error: function(error){
    		console.log(error);
    	}
    });
}
function sendZip(zip){
	$.ajax({
		url: '/wp-content/themes/madx/zip-code-search.php',
		type: 'POST',
		data: { zip:zip },
		dataType: 'json',
		success:function(data){
			console.log(data);
		}
	})
}
</script>

